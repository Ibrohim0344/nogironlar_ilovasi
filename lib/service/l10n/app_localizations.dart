  /// In en, this message translates to:
  /// **'SETTINGS'**
  String get settings;

  /// No description provided for @language.
  ///
  /// In en, this message translates to:
  /// **'LANGUAGE'**
  String get language;

  /// No description provided for @location.
  ///
  /// In en, this message translates to:
  /// **'LOCATION'**
  String get location;

  /// No description provided for @aboutApp.
  ///
  /// In en, this message translates to:
  /// **'ABOUT APP'**
  String get aboutApp;

  /// No description provided for @tapToStartListening.
  ///
  /// In en, this message translates to:
  /// **'Tap the speak button to start listening'**
  String get tapToStartListening;

  /// No description provided for @speechNotAvailable.
  ///
  /// In en, this message translates to:
  /// **'Speech is not available'**
  String get speechNotAvailable;

  /// No description provided for @listening.
  ///
  /// In en, this message translates to:
  /// **'Listening ...'**
  String get listening;

  /// No description provided for @scanImage.
